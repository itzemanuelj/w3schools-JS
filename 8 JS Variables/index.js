/// JS Variables

/// Automatically
x = 5;
y = 5;
z = x + y;
print(z);

/// Example using var

var x = 5;
var y = 5;
var z = x + y;
print(z);

/// Example using let

let x = 5;
let y = 5;
let z = x + y;
print(z);

/// Example using const

const x = 5;
const y = 5;
const z = x + y;
print(z);

/// Mixed Example

const price1 = 1;
const price2 = 1;
let total = price1 + price2;
print(total);

/// Just like in algebra, variables hold values:

let x = 5;
let y = 5;

let z = x + y;

/// The Assignment Operator =

x = x + 5;

